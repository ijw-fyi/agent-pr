/**
 * Count the number of lines of code changed in a diff
 * Counts lines starting with + or - (excluding diff headers like +++ and ---)
 */
function countDiffLOC(diff: string): number {
    const lines = diff.split('\n');
    let loc = 0;
    for (const line of lines) {
        // Count lines that start with + or - but not +++ or ---
        if ((line.startsWith('+') && !line.startsWith('+++')) ||
            (line.startsWith('-') && !line.startsWith('---'))) {
            loc++;
        }
    }
    return loc;
}

/**
 * Run the PR review agent with the given context
 */
export async function runReview(
    context: PRContext,
    recursionLimit: number = 100
): Promise<void> {
    // Check if PR exceeds max LOC limit
    const maxLOC = process.env.PR_AGENT_MAX_LOC ? parseInt(process.env.PR_AGENT_MAX_LOC, 10) : null;
    if (maxLOC !== null && !isNaN(maxLOC)) {
        const diffLOC = countDiffLOC(context.diff);
        console.log(`Diff LOC: ${diffLOC}, Max LOC: ${maxLOC}`);

        if (diffLOC > maxLOC) {
            console.log(`PR exceeds max LOC limit (${diffLOC} > ${maxLOC}), refusing to review`);

            const message = `## ⚠️ PR Too Large to Review

This pull request contains **${diffLOC.toLocaleString()} lines of code** changes, which exceeds the configured maximum of **${maxLOC.toLocaleString()} LOC**.

Please consider breaking this PR into smaller, more focused changes for a thorough review.

---
*This message was generated by PR Review Agent*`;

            await createPRComment(context.owner, context.repo, context.prNumber, message);
            return;
        }
    }

    // Reset cost tracking for this run
    resetRunningCost();
    const budget = getBudget();
    console.log(`💵 Budget: $${budget.toFixed(2)}`);

    // Create the model with OpenRouter backend and prompt caching
    const model = createCachedChatOpenAI();

    // Create the React agent
    const agent = createReactAgent({
        llm: model,
        tools,
    });

    // Build the initial context message
    const contextMessage = buildContextMessage(context);

    console.log("=".repeat(60));
    console.log("Starting PR Review Agent");
    console.log("=".repeat(60));
    console.log(`Model: ${process.env.MODEL}`);
    console.log(`Recursion Limit: ${recursionLimit}`);
    console.log(`Tools available: ${tools.map(t => t.name).join(", ")}`);
    console.log("=".repeat(60));

    console.log("\n📝 User Context Message:");
    console.log("─".repeat(60));
    console.log(contextMessage);
    console.log("─".repeat(60));

    // Stream the agent execution
    let stepCount = 0;
    const allMessages: (SystemMessage | HumanMessage | AIMessage | ToolMessage)[] = [
        new SystemMessage(getSystemPrompt(isWebSearchAvailable())),
        new HumanMessage(contextMessage),
    ];

    // Use AbortController to allow proper cancellation of the stream
    const abortController = new AbortController();

    const stream = await agent.stream(
        { messages: allMessages },
        { recursionLimit, signal: abortController.signal }
    );

    let budgetExceeded = false;
    for await (const chunk of stream) {
        stepCount++;
        processChunk(chunk, stepCount, allMessages);

        // Check budget after each step (only flag once)
        if (!budgetExceeded && isOverBudget()) {
            budgetExceeded = true;
            const cost = getRunningCost();
            console.log(`\n⚠️ Budget exceeded ($${cost.toFixed(4)} / $${budget.toFixed(2)}) - will wrap up after current tool calls`);
        }

        // If budget exceeded and we just got tool results, inject wrap-up and break
        // This ensures the LLM sees the budget notice before making more tool calls
        if (budgetExceeded && chunk.tools?.messages) {
            console.log("\n📝 Injecting wrap-up message after tool results...");
            allMessages.push(new HumanMessage("IMPORTANT BUDGET NOTICE: You are past your budget limit. STOP exploring the code immediately. Compile your findings from what you've already reviewed and start writing your review comments and summary."));
            // Abort the stream to stop background processing
            console.log("🛑 Aborting original stream...");
            abortController.abort();
            break;
        }
    }

    // If we broke out due to budget, create a fresh agent for wrap-up
    if (budgetExceeded) {
        console.log("\n📝 Creating fresh model and agent for wrap-up...");

        // Create a completely fresh model instance to avoid any state issues
        const wrapUpModel = createCachedChatOpenAI();

        // Create a new agent instance with the fresh model
        const wrapUpAgent = createReactAgent({
            llm: wrapUpModel,
            tools,
        });

        try {
            const wrapUpStream = await wrapUpAgent.stream(
                { messages: allMessages },
                { recursionLimit: 20 }
            );

            for await (const chunk of wrapUpStream) {
                stepCount++;
                processChunk(chunk, stepCount, allMessages);
            }
            console.log("� Wrap-up complete");
        } catch (error) {
            console.error("Wrap-up error:", error);
        }
    }

    const finalCost = getRunningCost();
    console.log(`\n${"=".repeat(60)}`);
    console.log(`Review completed. Total steps: ${stepCount}`);
    console.log(`💰 Final cost: $${finalCost.toFixed(4)} / $${budget.toFixed(2)} budget`);
    console.log("=".repeat(60));
}

/**
 * Build the context message for the agent
 */
function buildContextMessage(context: PRContext): string {
    let message = `# Pull Request Review Request

## PR Information
- **Title**: ${context.title}
- **Author**: ${context.author}
- **Branch**: ${context.headBranch} → ${context.baseBranch}

## PR Description
${context.description || "(No description provided)"}

## Changed Files Diff
\`\`\`diff
${truncateDiff(context.diff)}
\`\`\`
`;

    if (context.existingComments.length > 0) {
        message += `
## Existing Review Comments
${context.existingComments.map((c) => `- **${c.author}** on \`${c.path}\`: ${c.body}`).join("\n")}
`;
    }

    if (context.conversation.length > 0) {
        message += `
## PR Conversation
${context.conversation.map((c) => `- **${c.author}**: ${c.body}`).join("\n")}
`;
    }

    if (context.preferences) {
        message += `
## User Preferences
The following preferences have been learned from previous interactions. Please respect these when reviewing:
\`\`\`
${context.preferences}
\`\`\`
`;
    }

    message += `
## Your Task
Please review this pull request thoroughly. Use the tools available to:
1. Use list_directory to explore the project structure if needed
2. Read any files you need more context on
3. Use grep to search for patterns across the codebase
4. Leave inline comments on specific lines where you find issues
5. Submit your final review with a summary when done

Begin your review now.
`;

    return message;
}

/**
 * Truncate diff per file if it's too large
 */
function truncateDiff(diff: string): string {
    const MAX_LINES_PER_FILE = 500;
    const MAX_CHARS_PER_FILE = 10000;

    // Split by "diff --git" at start of line, keeping the delimiter with each part
    const parts = diff.split(/(?=^diff --git )/m);

    return parts.map(part => {
        if (!part.trim()) return part;

        if (part.length > MAX_CHARS_PER_FILE) {
            return part.slice(0, MAX_CHARS_PER_FILE) + "\n... (File diff truncated: exceeds 10k chars)\n";
        }

        const lines = part.split('\n');
        if (lines.length > MAX_LINES_PER_FILE) {
            return lines.slice(0, MAX_LINES_PER_FILE).join('\n') + "\n... (File diff truncated: exceeds 500 lines)\n";
        }

        return part;
    }).join('');
}
